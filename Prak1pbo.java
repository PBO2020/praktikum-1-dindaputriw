
package praktikum1pbo;

public class Prak1pbo {
    String nama, nik, ttl, jenis_kelamin;
    int gol_darah;
    
    public String getNama(){
        return nama;
    }
    public void setNama(String nama){
        this.nama=nama;
    }
    
    public String getNIK(){
        return nik;
    }
    public void setNIK(String nik){
        this.nik=nik;
    }
    
    public String getTTL(){
        return ttl;
    }
    public void setTTL(String ttl){
        this.ttl=ttl;
    }
    
    public String getJenisKelamin(){
        return jenis_kelamin;
    }
    public void setJenisKelamin(String jenis_kelamin){
        this.jenis_kelamin=jenis_kelamin;
    }
    
    public int getGolDarah(){
        return gol_darah;
    }
    public void setGolDarah(int gol_darah){
        this.gol_darah=gol_darah;
    }
    
    
}

